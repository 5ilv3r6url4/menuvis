QList<std::tuple<QString, double, QString>> Data_Delegate::get_tree_map_data(int row)
{
    if (m_ingredients_data->m_ingredients.isEmpty())
        return {};

    QList<std::tuple<QString, double, QString>> data_accumulator;
    Ingredients_Data::Ingredient* temp_ingredient = m_ingredients_data->m_ingredients[row];
    QList<std::tuple<Ingredients_Data::Ingredient*, double, QString>> temp_ingredients;
    for (int i = 0; i < m_recipes_data->m_recipes.size(); ++i) {
        temp_ingredients = m_recipes_data->m_recipes[i]->s_ingredients;
        for (int j = 0; j < temp_ingredients.size(); ++j) {
            if (std::get<0>(temp_ingredients[j])->s_number == temp_ingredient->s_number &&
                    std::get<0>(temp_ingredients[j])->s_supplier == temp_ingredient->s_supplier) {
                data_accumulator.append(std::make_tuple(m_recipes_data->m_recipes[i]->s_name,
                                                        std::get<1>(temp_ingredients[j]),
                                                        std::get<2>(temp_ingredients[j])));
            }
        }
    }
    return data_accumulator;
}

