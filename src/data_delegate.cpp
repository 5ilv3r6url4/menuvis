int Data_Delegate::add_staging_ingredient(bool flag, QString supplier, QString category, QString name, QString brand, QString uom, double amount,
                                          double price, QString number, QString description)
{
    int status = 0;
    if (flag && !m_ingredients_data->contains_staging_input(supplier, brand, name, number)) {
        Ingredients_Data::Ingredient* item = new Ingredients_Data::Ingredient(supplier, category, name, brand, uom, amount, price, number, description, {});
        m_ingredients_data->m_staging_input_ingredients.append(item);
        status = 1;
    }
    if (flag && m_ingredients_data->contains(supplier, brand, name, number)) {
        Ingredients_Data::Ingredient* item = new Ingredients_Data::Ingredient(supplier, category, name, brand, uom, amount, price, number, description, {});
        int temp_index = m_ingredients_data->contains(item);
        Ingredients_Data::Ingredient* curr_item = m_ingredients_data->m_ingredients[temp_index];
        item->s_amount = curr_item->s_amount;
        item->s_brand = curr_item->s_brand;
        item->s_category = curr_item->s_category;
        item->s_description = curr_item->s_description;
        item->s_name = curr_item->s_name;
        item->s_uom = curr_item->s_uom;
        m_ingredients_data->m_staging_output_ingredients.append(item);
        status = 2;
    }
    else if (!flag && !m_ingredients_data->contains_staging_output(supplier, brand, name, number)) {
        Ingredients_Data::Ingredient* item = new Ingredients_Data::Ingredient(supplier, category, name, brand, uom, amount, price, number, description, {});
        m_ingredients_data->m_staging_output_ingredients.append(item);
        status = 1;
    }
    return status;
}

double Data_Delegate::get_staging_ingredient_previous_price(int row)
{
    Ingredients_Data::Ingredient* current_ingredient = m_ingredients_data->m_staging_output_ingredients[row];
    int index = m_ingredients_data->contains(current_ingredient);
    if (m_ingredients_data->contains(current_ingredient) != -1) {
        return m_ingredients_data->m_ingredients[index]->s_price;
    }
    else
        return -1.0;
}

