double Data_Delegate::aggregate_ingredient_category_volatilties(QString category)
{
    double running_sum = 0.0;
    for (int i = 0; i < m_ingredients_data->m_ingredients.size(); ++i) {
        Ingredients_Data::Ingredient* temp_ingredient = m_ingredients_data->m_ingredients[i];
        if (temp_ingredient->s_category == category)
            running_sum += temp_ingredient->s_volatility;
    }
    return running_sum;
}

QList<int> Data_Delegate::get_ingredient_indices_by_category(QString category)
{
    QList<int> indices;
    for (int i = 0; i < m_ingredients_data->m_ingredients.size(); ++i) {
        if (m_ingredients_data->m_ingredients[i]->s_category == category) {
            indices.append(i);
        }
    }
    return indices;
}

